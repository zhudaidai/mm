<template>
  <div id="app">
     <a @click="one" href="http://www.baidu.com">阻止百度</a>
    <hr>
    <a @click="two(10, $event)" href="http://www.baidu.com">阻止去百度</a>
  </div>
</template>

<script>


export default {
  data() { // 格式固定, 定义vue数据之处
    return {  // key相当于变量名
     
    }
      
  },
  methods: {
    // 1. 事件触发, 无传值, 可以直接获取事件对象
    one(e){
      e.preventDefault()
    },
    // 2. 事件触发, 传值, 需要手动传入$event
    two(num, e){
      e.preventDefault()
    }
  }

}
</script>

